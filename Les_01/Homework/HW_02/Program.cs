// Сравнение двух числе
Console.WriteLine("Введите число a: ");
int a =int.Parse(Console.ReadLine());
Console.WriteLine("Введите b: ");
int b =int.Parse(Console.ReadLine());
if (a>b)
{
    Console.WriteLine ("Большее число");
    Console.WriteLine (a);
}
else
{
    Console.WriteLine("Большее число");
    Console.WriteLine (b);
}